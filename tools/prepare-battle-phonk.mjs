/**
 * Downloads a CC0 funk loop for combat BGM → public/audio/battle.ogg
 * Not viral Brazilian phonk (copyrighted style) — CC0 funk loop as royalty-safe default.
 * Replace public/audio/battle.ogg with your own licensed phonk export anytime.
 *
 * Run: npm run music:battle
 * Requires ffmpeg on PATH for OGG conversion (optional: keeps .mp3 if missing).
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { execSync } from "child_process";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const audioDir = path.join(root, "public", "audio");

/** CC0 — iamoneabe, OpenGameArt "Funky Menu Loop" (longer edit) */
const SOURCE = {
  url: "https://opengameart.org/sites/default/files/funkymenuloop-longer.mp3",
  title: "Funky Menu Loop (longer)",
  artist: "iamoneabe",
  license: "CC0 1.0",
  page: "https://opengameart.org/content/funky-menu-loop",
};

function hasFfmpeg() {
  try {
    execSync("ffmpeg -version", { stdio: "ignore" });
    return true;
  } catch {
    return false;
  }
}

async function main() {
  fs.mkdirSync(audioDir, { recursive: true });
  const mp3Path = path.join(audioDir, "battle-src.mp3");
  const oggPath = path.join(audioDir, "battle.ogg");
  const mp3Out = path.join(audioDir, "battle.mp3");

  console.log("Downloading CC0 funk loop for battle BGM…");
  const res = await fetch(SOURCE.url);
  if (!res.ok) throw new Error(`Download failed HTTP ${res.status}`);
  const buf = Buffer.from(await res.arrayBuffer());
  fs.writeFileSync(mp3Path, buf);
  console.log(`  wrote battle-src.mp3 (${buf.length} bytes)`);

  if (hasFfmpeg()) {
    execSync(
      `ffmpeg -y -i "${mp3Path}" -c:a libvorbis -q:a 6 "${oggPath}"`,
      { stdio: "inherit" }
    );
    console.log(`  wrote ${path.relative(root, oggPath)}`);
  } else {
    fs.copyFileSync(mp3Path, mp3Out);
    console.warn("  ffmpeg not found — using public/audio/battle.mp3 instead of .ogg");
  }

  appendCredits();
  console.log("\nDone. Combat uses /audio/battle.ogg (or .mp3) when present.");
  console.log(
    "For real Brazilian phonk: export a royalty-free track (e.g. Pixabay license) to public/audio/battle.ogg"
  );
}

function appendCredits() {
  const block = [
    "",
    "battle.ogg (or battle.mp3)",
    `  Title: ${SOURCE.title}`,
    `  Artist: ${SOURCE.artist}`,
    `  License: ${SOURCE.license}`,
    `  Source: ${SOURCE.page}`,
    "  Notes: Default combat BGM (funk loop). Replace file for custom phonk.",
    "",
  ].join("\n");

  for (const rel of ["CREDITS.txt", "public/CREDITS.txt"]) {
    const p = path.join(root, rel);
    if (!fs.existsSync(p)) continue;
    let text = fs.readFileSync(p, "utf8");
    if (!text.includes("battle.ogg")) {
      text = text.replace(/Generated by:/, `${block}Generated by:`);
      fs.writeFileSync(p, text);
    }
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
